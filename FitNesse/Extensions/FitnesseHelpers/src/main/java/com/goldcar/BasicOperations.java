package com.goldcar;

/**
 * Operaciones básicas para operar con símbolos en tablas
 */

public class BasicOperations {

    public BasicOperations() {
    }

   public int addTo(int a, int b) {
        return a + b;
    }

    public float addToFloat(float a, float b) {
        return a + b;
    }

    public int multiplyTo(int a, int b) {
        return a * b;
    }

    public float multiplyToFloat(float a, float b) {
        return a * b;
    }

    public String echo(String s) {
        return s;
    }

}
